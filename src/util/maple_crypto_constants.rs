#[inline]
pub fn get_trimmed_user_key(user_key: &[u8]) -> [u8; 32] {
    let mut trimmed_key = [0; 32];

    for i in (0..128).step_by(16) {
        let index = i / 4;
        trimmed_key[index] = user_key[i];
    }

    trimmed_key
}
